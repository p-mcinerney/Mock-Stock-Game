import time
import random
class Stock:
    def __init__(self):
        self.name = None
        self.initial_value = random.randint(5, 100)

    def update_value():
        Stock.initial_value += 1

        while True:
            Stock.update_value()
            time.sleep(10)
            
class Player:
    def __init__(self, name):
        self.username = name
        self.balance = 0
        self.numstock = 0

    def sell():
        Player.balance + Stock.initial_value
        Player.numstock - 1
        return

    def buy():
        Player.balance - Stock.initial_value
        Player.numstock + 1
        return

    def view_market():
        print(Stock.initial_value)
        return

